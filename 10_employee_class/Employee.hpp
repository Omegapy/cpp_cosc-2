  /*
  
      Contains the class Employee Declarations

      The class creates employee objects with the arguments
        (private) id, first and last name, pay rate, and pay
        (static) number of employees and base pay
      
  */
  ///////////////////////////////////////////////////////////////////////////////////////////////////////////////

#ifndef EMPLOYEE_HPP
#define EMPLOYEE_HPP

#ifdef _MSC_VER
    #pragma once
#endif  // _MSC_VER

#include "header.hpp"

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//-------------------------------------------- Class Employee ---------------------------------------------------
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
class Employee
{
    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Variables (Private)
    //----------------------------------------------------------------------------------
    
    // Employee name
    string firstName,
           lastName;
    unsigned id; // Employee's ID number
    double payRate, // Employee's pay rate
           pay; // Employee's pay  = ( 1 + payRate) * basePay

    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Static variables (Private)
    //----------------------------------------------------------------------------------
    
    static unsigned numEmployees, // Number of employees
                    basePay; // Base pay of all employees

public:

    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Variables (Public)
    //----------------------------------------------------------------------------------
    // None

    /*---------------------------------------------------

        Default construtor 

     ----------------------------------------------------*/
    Employee();

    /*---------------------------------------------------

        Constructor-1

     ----------------------------------------------------*/
    Employee(string firstName, string lastName, double payRate);

    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Accessor Methods
    //----------------------------------------------------------------------------------

    /*----------------------------------------------------

        Returns the employee's first name

     -----------------------------------------------------*/
    string getFirstName();

    /*----------------------------------------------------

        Returns the employee's last name

     -----------------------------------------------------*/
    string getLastName();

    /*----------------------------------------------------

        Returns the employee's ID

     -----------------------------------------------------*/
    unsigned getId();

    /*----------------------------------------------------

        Returns the employee's pay rate

     -----------------------------------------------------*/
    double getPayRate();

    /*----------------------------------------------------

        Returns the employee's pay 

     -----------------------------------------------------*/
    double getPay();

    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Static accessor methods 
    //----------------------------------------------------------------------------------

    /*----------------------------------------------------

        Returns base pay

     -----------------------------------------------------*/
    static unsigned getBasePay();

    /*----------------------------------------------------

        Returns the number of employees

     -----------------------------------------------------*/
    static unsigned getNumEmployees();


    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Mutator Methods
    //---------------------------------------------------------------------------------

    /*----------------------------------------------------

        Sets the employee's first name

     -----------------------------------------------------*/
    void setFirstName(string firstName);

    /*----------------------------------------------------

        Sets the employee's last name

     -----------------------------------------------------*/
    void setLastName(string lastName);

    /*----------------------------------------------------

        Sets the employee's ID

             Warning duplicate,
             modifying the employee's ID
             can result in a duplicate ID.
             "Two employees with the same ID"

     -----------------------------------------------------*/
    void setId(unsigned id);

    /*----------------------------------------------------

        Sets the employee's pay rate

     -----------------------------------------------------*/
    void setPayRate(double payRate);


    /*----------------------------------------------------

        Sets the employee's pay

     -----------------------------------------------------*/
    void setPay(double pay);

    //----------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------

    // Static mutators methods 
    //----------------------------------------------------------------------------------

    /*----------------------------------------------------

        Sets base pay

     -----------------------------------------------------*/
    static void setBasePay(unsigned baseRate);

    /*----------------------------------------------------

        Sets the number of employees

            Warning duplicate,
            modifying the employees number can result
            in employees duplicate IDs.
            subtracting from the employees number value,
            and then initializing new employees,
            may cause the new employees
            to acquirer claimed employee IDs.

     -----------------------------------------------------*/
    static void setNumEmployees(unsigned numEmployees);


};
#endif

